 La borne nous indique alors la somme dépensée pour le trajet ainsi que le montant restant. On n'a d'atteindre une somme négative qu'une seule fois. On doit recharger la carte sinon on ne passe pas. <br />
Si on s'est trompé de station on peut évidemment en ressortir sans prendre le métro. Il suffit d'attendre une minute à rien faire et surtout être prêt à perdre HK$3 pour rien. Mais bon, au moins on peut ressortir.</p>  

<p>Bon ensuite prendre le métro...après 15 000km il y a forcément des surprises. Déjà en théorie, il faut laisser les gens descendre du métro avant de monter. Bon ça peut paraître logique pour certains mais ici ca ne l'est pas. Je fais 1m65 donc je la ferme mais en général un européen qui se ramène avec ses 1m85, bizarremment on le laisse toujours descendre avant de monter. Les gens courent presque pour rentrer dedans et je vous raconte pas la guerre pour prendre une place assise. Chacun pour soi, Dieu pour tous, sans le Dieu évidemment.</p>


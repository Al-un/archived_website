$(document).ready(function(){
  
  
  /* *******************************************************************************************
  *                 MAIN DISPLAY PANEL
  ******************************************************************************************* */
  
  /* Addr Type checkboxes */
  var addrCateId = 0;
  $('input[id^="AddrCate"]').click(function() {
    addrCateId = $(this).attr('id').substring(8);
    $('input[id^="AddrSubCate' + addrCateId + '"]').prop('checked', $(this).is(":checked"));
  });

  /* Addr Area checkboxes */
  var addrCityId = 0;
  $('input[id^="AddrArea"]').click(function() {
    addrCityId = $(this).attr('id').substring(8);
    $('input[id^="AddrCity' + addrCityId + '"]').prop('checked', $(this).is(":checked"));
  });


  /* *******************************************************************************************
  *                 ADMIN PANEL
  ******************************************************************************************* */



});




 






 
 
 
 
 
 
<p>Les bornes sont assez efficaces. On passe la carte, un petit bip et on peut passer le tourniquet. Par contre si on tente de gruger, ça déclenche une alarme d'enfer qui réveille toute la station d'un coup. Déjà entendu du moins. Bref peu de gens fraudent ici. Pour la sortie, pareil, on revalide après avec la même carte.<br />
<div style='text-align:center;'>
<img src = 'img/mtr_valid.jpg' title = 'Il nous reste combien?' alt = 'MTR valid Octopus' height = '200' />

<p>Evidemment pendant le trajet, surtout les premières fois, faut pas avoir l'air abruti lorsqu'après un virage on se retrouve collé contre la vitre ou alors écroulé sur la banquette arri&egrave;re si le virage est dans l'autre sens. Bon à force, on sait à quoi s'attendre.</p>

<p>Sinon heureusement à Hong Kong, les chauffeurs de taxi parlent pour la plupart anglais et mandarin. J'ai même déjà rencontré un chauffeur de taxi qui parlait Français !!! Bon il a habité 3 à Paris 13ème...A Macao, ce n'est pas toujours évident de trouver un chauffeur qui parle anglais alors le mandarin est fortement conseillé, du moins quelques notions. J'ai en tête quelques expériences à Macao qui me font penser qu'heureusement je connais quelques mots en madarins...</p> 



<p>Le temps de trajet moyen entre deux stations n'est que de quelques minutes. Pour faire toute la ligne rouge (Tsuen Wan - Central) il faut compter environ 30-40 minutes. L'Airport Express (ligne verte)est une exception. Il relie en effet l'aéroport à la station de Kowloon en 20 minutes. Il coute par contre plus cher. Pour faire Airport-Tsing Yi (première station de l'Airport Express en venant de l'aéroport), il faut compter HK$60 alors que faire Tsuen Wan-Central ne coute "que" HK$11.5</p>

<p>Payer c'est bien beau mais on paie quoi? Bien sûr, on peut acheter des tickets aller simple comme à Paris. Il existe évidemment des pass (pass à la journée ou pass pour tel réseau de transport, etc) et surtou il existe un équivalent de la Carte Orange combiné avec un équivalent de la Carte Moneo, si ça existe encore ces machins mais personnellement je n'en ai jamais vu. C'est la super carte <i>Octopus</i> !!! Bon le nom n'est pas transcendant je l'avoue mais ici le concept du <i>style</i> n'est pas forcément le même qu'en Europe...Bref c'est une carte, on la paie HK$50 on doit la charger au début avec HK$100, ce qui fait HK$150 pour la prendre. On la recharge ensuite en liquide (par tranche de billet de HK$100 ou HK$50) ou par virement bancaire. Pour ce dernier mode de rechargement, il faut retirer une Octopus auprès de la banque.</p>



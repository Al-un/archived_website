<p>Contrairement à la RATP, le MTR est efficace. Ca c'est la première impression qui m'est ressortie. Du premier coup d'oeil, le réseau est un peu moins étoilé. Bien sûr, la géographie de Hong Kong y est pour quelque chose, ce n'est pas grand donc on quadrille au maximum. Mais ce qui m'a impressionné, c'est par exemple la possibilité de "couper" la ligne rouge en venant de Tsuen Wan pour aller à Hong Kong/Central via la ligne orange. Le plan est ci-dessous</p>

<div style = 'text-align:center;'>

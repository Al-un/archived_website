<p>Alors monter dans un taxi se fait de deux manières. Soit on a la classe, soit on ne l'a pas. Avoir la classe, c'est par exemple les taxis qui font la queue et quand on sort du métro, hop le chauffeur de taxi nous ouvre la porte (ils ont une commande pour ouvrir automatiquement la porte arrière côté passager), on monte dedans et limite ils nous ouvre le coffre si on a un bagage pour mettre dedans. HK$5 par bagage par contre. Quand on n'a moins la classe, c'est par exemple à Mong Kok (quartier blindé où parfois on ne voit pas à 2 mètres tellement que c'est noir de monde) ben on chercher désespéremment un taxi car c'est le bordel le plus total, on trouve un taxi. Le feu est rouge. On court vers le taxi et le but du jeu est que tout le monde rentre dedans, faire comprendre sa destination avant que le feu ne passe au vert. J'ai déjà testé, ça peut se révéler sportif. Bref ca peut donner environ 30 secondes pour courir/monter dedans/parler.</p>

